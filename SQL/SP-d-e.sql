    DROP PROCEDURE SP_UPDATE_STUDENT_INFO;
GO

-- Create new procedure
CREATE PROCEDURE SP_UPDATE_STUDENT_INFO
    @MASV NVARCHAR(20),
    @HOTEN NVARCHAR(100),
    @DIACHI NVARCHAR(200)
AS
BEGIN
    SET NOCOUNT ON;

    UPDATE SINHVIEN
    SET HOTEN = @HOTEN,
        DIACHI = @DIACHI
    WHERE MASV = @MASV;
END;
GO










